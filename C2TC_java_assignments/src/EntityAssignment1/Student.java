package EntityAssignment1;

public class Student {

	private int roll;
    private String name;
    private String dept;
	public int getRoll() {
		return roll;
	}
	public void setRoll(int roll) {
		this.roll = roll;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	@Override
	public String toString() {
		return "Student rollno = "+roll+" Student name = "+name+" Student dept = "+dept;
	}

}
